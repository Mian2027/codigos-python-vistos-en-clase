# Version de Python: Las listas (el equivalente de Python a los arreglos (arrays)) han sido parte
# de Python desde la version 1.0 (enero de 1994).

# Creacion de arreglos (arrays) en Python
# En Python, el equivalente de los arreglos (arrays) en otros lenguajes es la estructura de datos lista (list).
# Las listas son versatiles, lo que te permite almacenar colecciones de elementos en una
# sola variable.

# Creando una lista (arreglo/array) simple de numeros
numbers = [1, 2, 3, 4, 5]
print('Numeros:', numbers)  # Salida: Numeros: [1, 2, 3, 4, 5]

# Las listas pueden contener diferentes tipos de datos
mixed_list = [1, "two", 3.0, True]
# Salida: Lista mixta: [1, 'two', 3.0, True]
print('Lista mixta:', mixed_list)

# Las listas tambien pueden estar vacias y llenarse despues
empty_list = []
print('Lista vacia:', empty_list)  # Salida: Lista vacia: []

# Nota: Las listas en Python son dinamicas, lo que significa que puedes cambiar su tamano
# y contenido durante la ejecucion del programa. Esto las convierte en una de las
# estructuras de datos mas flexibles en Python.
