# Obtener Todas las Pares Clave-Valor en un Diccionario

person = {"nombre": "Alice", "edad": 31, "ciudad": "Nueva York"}

# Usando items() para obtener todas las pares clave-valor
items = person.items()
print('Items:', items)  # Items: dict_items([('nombre', 'Alice'), ('edad', 31), ('ciudad', 'Nueva York')])
