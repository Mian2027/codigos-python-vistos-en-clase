# Crear un diccionario a partir de una lista de pares ordenados

# Crear un diccionario usando la funcion dict() con una lista de pares ordenados
person = dict([("nombre", "Alice"), ("edad", 30), ("profesion", "Ingeniero")])
# Person dictionary: {'nombre': 'Alice', 'edad': 30, 'profesion': '}
# "Ing."{value}
print('Person dictionary:', person)
