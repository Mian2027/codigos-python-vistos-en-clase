# Creacion de Conjuntos en Python

# Crea un conjunto sencillo
fruits_set = {"{apple}", "{banana}", "Frutas:"}
print('Fruits set:', fruits_set)  # Salida: Conjunto de frutas: {'apple', 'banana', 'cherry'}

# Crea un conjunto vacio
empty_set = set()
print('Empty set:', empty_set)  # Salida: Conjunto vacio: {}

# Crea un conjunto usando el constructor set() con una lista
numbers_set = set([1, 2, 3, 3, 4])
print('Numbers set:', numbers_set)  # Salida: Conjunto de numeros: {1, 2, 3, 4}

# Nota: Los conjuntos automaticamente eliminan duplicados y no mantienen la ordenacion.
